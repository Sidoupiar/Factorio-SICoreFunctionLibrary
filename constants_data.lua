return
{
	base = "CoreFunctionLibrary" ,
	autoLoad = true ,
	autoName = true
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- [pictureSource] 可以把贴图包路径修改为其他 mod , 只支持一个 mod
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- [picturePaths] 可以存储多个贴图包路径 , 用来支持多个附属贴图 mod
	-- ------------------------------------------------------------------------------------------------
	-- 具体格式为 :
	--     modPath1 = { typeList } ,
	--     modPath2 = { typeList }
	-- 可用的 modPath 应为附属的贴图 mod 名称
	-- 可用的 type 应为 data prototype 支持的 type 类型 , 只有符合这个类型的 prototype 才会适用路径
	-- ------------------------------------------------------------------------------------------------
	-- 列表是有序的 , 定义的顺序为应用的顺序
	-- 定义一个 prototype 的时候 , 会根据列表从上到下根据 prototype 的类型匹配贴图路径 , 选择匹配的路径
	-- 如果没有匹配的路径 , 则使用通过 base 或 pictureSource 属性定义的路径
	-- 可以通过 Constants.mainPicturePath = index 属性修改路径的优先级
	-- Constants.mainPicturePath 小于 1 的话 , 则不会影响路径的优先级
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- [soundSource] 可以把声音包路径修改为其他 mod , 只支持一个 mod
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- [damage] 和 [categories] 自动构建
	-- ------------------------------------------------------------------------------------------------
	-- 具体格式为 :
	-- damage = { "伤害类别1" , "伤害类别2" , "伤害类别3" }
	-- categories =
	-- {
	--     [SITypes.category.x] = { "类别1" , "类别2" , "类别3" } ,
	--     [SITypes.category.y] = { "类别1" , "类别2" , "类别3" }
	-- }
	-- 其中 SITypes.category.x 和 SITypes.category.y 为 SITypes.category 的可用键
	-- 所有类别的名称都不会自动添加任何内容 , 最终使用原始内容进行数据注册
	-- ------------------------------------------------------------------------------------------------
	-- 当在 data 模块中时 , 如果上面的 autoLoad 属性为 true
	-- 则会在初始化 Constants 时把 [damage] 和 [categories] 中定义的类别都注册进 data 表中
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- [finalSettingDataList] 是用来把 mod 设置的值替换成固定配置的值的 , 主要供服务器等特殊场合使用
	-- 这种方式仅对 SI 系列 mod 有效(当然也包括使用了完全一样的写法的 mod)
	-- ------------------------------------------------------------------------------------------------
	-- 具体格式为 :
	--     name1 = value1 ,
	--     name2 = value2
	--     ...
	-- 可用的 name 值详见同 mod 下 constants.lua 中的 [settings] 内的 key 部分
	-- 可用的 value 值依据实际设置类型而定 , 使用非法的值会炸掉游戏
	-- ------------------------------------------------------------------------------------------------
	-- 对于被替换的设置 , 游戏内设置则会完全变成摆设 , 不会起任何作用 , 实际引用的是这里配置的 value 值
	-- 这里只能替换同 mod 创建的 mod 设置的值 , 如果包含了其他的设置 , 则那个设置会被忽略
	-- ------------------------------------------------------------------------------------------------
	-- 栗子 :
	-- finalSettingDataList =
	-- {
	--     writelog = true ,
	--     debug = true
	-- }
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	-- ------------------------------------------------------------------------------------------------
	damage =
	{
		"sicfl-blood" ,
		"sicfl-water" ,
		"sicfl-ice" ,
		"sicfl-radiation" ,
		"sicfl-disease" ,
		"sicfl-energy" ,
		"sicfl-fear" ,
		"sicfl-spirit" ,
		"sicfl-twist" ,
		"sicfl-void"
	}
}